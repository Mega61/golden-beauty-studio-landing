// Build-time generator for the Lookbook manifest.
//
// Walks `public/lookbook/<category>/*.{jpg,jpeg,png,webp}` and writes
// `src/data/lookbook-manifest.ts` — a typed, readonly array consumed by the
// Lookbook component for both the editorial-mosaic and filtered grid views.
//
// Categories are derived from subfolder names (kebab-case). Captions are
// derived from filenames (kebab-case → "Sentence case").
//
// Idempotent: regenerates only when a source file is newer than the manifest.
// Runs in `predev` and `prebuild` (see package.json).

import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const SRC_DIR = path.join(ROOT, "public", "lookbook");
const OUT_FILE = path.join(ROOT, "src", "data", "lookbook-manifest.ts");

const ALLOWED_EXT = new Set([".jpg", ".jpeg", ".png", ".webp"]);
// Folders to ignore inside public/lookbook/ (not real categories)
const IGNORED_FOLDERS = new Set([".DS_Store", "thumbs.db"]);

function captionFromBasename(basename) {
  const cleaned = basename.replace(/[-_]+/g, " ").trim();
  if (!cleaned) return basename;
  return cleaned.charAt(0).toUpperCase() + cleaned.slice(1);
}

async function ensureDir(dir) {
  await fs.mkdir(dir, { recursive: true });
}

async function listCategories() {
  let entries;
  try {
    entries = await fs.readdir(SRC_DIR, { withFileTypes: true });
  } catch {
    return [];
  }
  return entries
    .filter(
      (e) =>
        e.isDirectory() &&
        !e.name.startsWith(".") &&
        !IGNORED_FOLDERS.has(e.name.toLowerCase()),
    )
    .map((e) => e.name)
    .sort();
}

async function listImagesIn(category) {
  const dir = path.join(SRC_DIR, category);
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const out = [];
  for (const e of entries) {
    if (!e.isFile()) continue;
    const ext = path.extname(e.name).toLowerCase();
    if (!ALLOWED_EXT.has(ext)) continue;
    const basename = path.basename(e.name, ext);
    out.push({
      category,
      src: `/lookbook/${category}/${e.name}`,
      basename,
      caption: captionFromBasename(basename),
      mtime: (await fs.stat(path.join(dir, e.name))).mtimeMs,
    });
  }
  return out.sort((a, b) => a.basename.localeCompare(b.basename));
}

async function needsRebuild(items) {
  try {
    const outStat = await fs.stat(OUT_FILE);
    const newestSrc = items.reduce((acc, it) => Math.max(acc, it.mtime), 0);
    return newestSrc > outStat.mtimeMs;
  } catch {
    return true;
  }
}

async function main() {
  await ensureDir(path.dirname(OUT_FILE));

  const categories = await listCategories();
  if (categories.length === 0) {
    console.warn(
      "[build-lookbook-manifest] no category folders found under public/lookbook/",
    );
  }

  const allItems = [];
  for (const cat of categories) {
    const items = await listImagesIn(cat);
    allItems.push(...items);
  }

  if (!(await needsRebuild(allItems))) {
    console.log(
      "[build-lookbook-manifest] manifest up to date, skipping regenerate.",
    );
    return;
  }

  const categoryUnion = categories.length
    ? categories.map((c) => `  | ${JSON.stringify(c)}`).join("\n")
    : "  | string";

  const stripped = allItems.map((it) => ({
    category: it.category,
    src: it.src,
    basename: it.basename,
    caption: it.caption,
  }));

  const body = stripped
    .map(
      (it) =>
        `  { category: ${JSON.stringify(it.category)}, src: ${JSON.stringify(it.src)}, basename: ${JSON.stringify(it.basename)}, caption: ${JSON.stringify(it.caption)} },`,
    )
    .join("\n");

  const out = `// AUTO-GENERATED by scripts/build-lookbook-manifest.mjs — DO NOT EDIT.
// Sourced from public/lookbook/<category>/*.{jpg,jpeg,png,webp}.

export type LookbookCategory =
${categoryUnion};

export type LookbookItem = {
  category: LookbookCategory;
  src: string;
  basename: string;
  caption: string;
};

export const LOOKBOOK: readonly LookbookItem[] = [
${body}
] as const;
`;

  await fs.writeFile(OUT_FILE, out, "utf8");
  console.log(
    `[build-lookbook-manifest] wrote ${stripped.length} item(s) across ${categories.length} category folder(s) → src/data/lookbook-manifest.ts`,
  );
}

main().catch((err) => {
  console.error("[build-lookbook-manifest] failed:", err);
  process.exit(1);
});
